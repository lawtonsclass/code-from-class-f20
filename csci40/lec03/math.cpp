#include <cmath>
#include <iostream>
using namespace std;

int main() {
  cout << cos(0) << endl;
  cout << atan(1) * 4 << endl;

  cout << exp(1) << endl;
  cout << pow(2.5, 2) << endl;
  cout << sqrt(2) << endl;

  cout << abs(-42) << endl;
  cout << abs(2) << endl;

  cout << round(3.14) << endl;
  cout << round(3.5) << endl;

  return 0;
}
